package certificacao_java11.classes;

public class ClassWithLongName {

	String name;
	
	public ClassWithLongName() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	public String toString() {
		return "ClassWithLongName{" + 
				"name='" + name + "\"}'";
	}
}
